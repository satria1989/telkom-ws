package id.co.itasoft.telkom.oss.plugin.model;

/**
 *
 * @author tarki
 */
public class ApiConfig {
    
    public String url;
    public String apiId;
    public String apiKey;
    public String apiToken;
    public String jUsername;
    public String jPassword;
    public String clientId;
    public String clientSecret;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getApiId() {
        return apiId;
    }

    public void setApiId(String apiId) {
        this.apiId = apiId;
    }

    public String getApiKey() {
        return apiKey;
    }

    public void setApiKey(String apiKey) {
        this.apiKey = apiKey;
    }

    public String getApiToken() {
        return apiToken;
    }

    public void setApiToken(String apiToken) {
        this.apiToken = apiToken;
    }

    public String getjUsername() {
        return jUsername;
    }

    public void setjUsername(String jUsername) {
        this.jUsername = jUsername;
    }

    public String getjPassword() {
        return jPassword;
    }

    public void setjPassword(String jPassword) {
        this.jPassword = jPassword;
    }

    public String getClientId() {
        return clientId;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }

    public String getClientSecret() {
        return clientSecret;
    }

    public void setClientSecret(String clientSecret) {
        this.clientSecret = clientSecret;
    }
    
    
    
    
    
}
